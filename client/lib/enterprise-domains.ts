// Helper function to get critical data lineage paths
export function getCriticalDataPath(
  sourceDomainId: string,
  targetDomainId: string,
): BankingDomain[] {
  // Simple BFS to find path between domains
  const queue: { domain: BankingDomain; path: BankingDomain[] }[] = [];
  const visited = new Set<string>();

  const sourceDomain = getDomainById(sourceDomainId);
  if (!sourceDomain) return [];

  queue.push({ domain: sourceDomain, path: [sourceDomain] });
  visited.add(sourceDomainId);

  while (queue.length > 0) {
    const { domain, path } = queue.shift()!;

    if (domain.id === targetDomainId) {
      return path;
    }

    const related = getRelatedDomains(domain.id);
    for (const { domain: relatedDomain } of related) {
      if (!visited.has(relatedDomain.id)) {
        visited.add(relatedDomain.id);
        queue.push({ domain: relatedDomain, path: [...path, relatedDomain] });
      }
    }
  }

  return [];
}

