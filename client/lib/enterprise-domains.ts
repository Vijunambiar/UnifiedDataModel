export type DomainPriority = "P0" | "P1" | "P2" | "P3";
export type DomainComplexity = "Low" | "Medium" | "High" | "Very High";
export type BusinessValue = "Critical" | "High" | "Medium";
export type DataQualityTier =
  | "Tier 1 (Critical)"
  | "Tier 2 (Important)"
  | "Tier 3 (Standard)";
