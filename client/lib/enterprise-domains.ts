// Helper function to get domain by ID
export function getDomainById(id: string): BankingDomain | undefined {
  return bankingDomains.find((domain) => domain.id === id);
}

// Helper function to get all upstream dependencies for a domain
export function getUpstreamDependencies(domainId: string): BankingDomain[] {
  const domain = getDomainById(domainId);
  if (!domain || !domain.relationships) return [];

  const upstreamIds = domain.relationships
    .filter((rel) => rel.type === "depends_on")
    .map((rel) => rel.targetDomainId);

  return upstreamIds
    .map((id) => getDomainById(id))
    .filter((d): d is BankingDomain => d !== undefined);
}

// Helper function to get all downstream consumers for a domain
export function getDownstreamConsumers(domainId: string): BankingDomain[] {
  const consumers: BankingDomain[] = [];

  bankingDomains.forEach((domain) => {
    if (domain.relationships) {
      const dependsOnTarget = domain.relationships.some(
        (rel) =>
          (rel.type === "depends_on" || rel.type === "feeds_into") &&
          rel.targetDomainId === domainId,
      );
      if (dependsOnTarget) {
        consumers.push(domain);
      }
    }
  });

  return consumers;
}

// Helper function to get related domains (all relationship types)
export function getRelatedDomains(domainId: string): {
  type: RelationshipType;
  domain: BankingDomain;
  description: string;
  criticality: "high" | "medium" | "low";
}[] {
  const domain = getDomainById(domainId);
  if (!domain || !domain.relationships) return [];

  return domain.relationships
    .map((rel) => ({
      type: rel.type,
      domain: getDomainById(rel.targetDomainId),
      description: rel.description,
      criticality: rel.criticality,
    }))
    .filter(
      (
        item,
      ): item is {
        type: RelationshipType;
        domain: BankingDomain;
        description: string;
        criticality: "high" | "medium" | "low";
      } => item.domain !== undefined,
    );
}

